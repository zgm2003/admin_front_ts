import { readdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, relative, resolve, sep } from 'node:path'
import { fileURLToPath } from 'node:url'

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const viewsRoot = resolve(repositoryRoot, 'src/views/Main')
const outputPath = resolve(repositoryRoot, 'src/modules/routing/generated/local-views.ts')
const checkOnly = process.argv.includes('--check')
const unsupportedArguments = process.argv.slice(2).filter((argument) => argument !== '--check')

if (unsupportedArguments.length > 0) {
  throw new Error(`unsupported arguments: ${unsupportedArguments.join(', ')}`)
}

async function collectIndexViews(directory) {
  const entries = await readdir(directory, { withFileTypes: true })
  const files = []
  for (const entry of entries) {
    const path = resolve(directory, entry.name)
    if (entry.isDirectory()) {
      files.push(...await collectIndexViews(path))
    } else if (entry.isFile() && entry.name === 'index.vue') {
      files.push(path)
    }
  }
  return files
}

function viewKey(file) {
  const normalized = relative(viewsRoot, file).split(sep).join('/')
  if (!normalized.endsWith('/index.vue')) {
    throw new Error(`view registry input is not an index.vue file: ${normalized}`)
  }
  return normalized.slice(0, -'/index.vue'.length)
}

function assertUnique(keys) {
  const seen = new Map()
  for (const key of keys) {
    const normalized = key.normalize('NFC').toLocaleLowerCase('en-US')
    const previous = seen.get(normalized)
    if (previous !== undefined) {
      throw new Error(`duplicate normalized local view key: ${previous} and ${key}`)
    }
    seen.set(normalized, key)
  }
}

function render(keys) {
  const keyLines = keys.map((key) => `  ${JSON.stringify(key)},`).join('\n')
  const loaderLines = keys.map((key) => (
    `  ${JSON.stringify(key)}: () => import(${JSON.stringify(`../../../views/Main/${key}/index.vue`)}),`
  )).join('\n')
  return `// Generated by scripts/generate-view-registry.mjs. Do not edit manually.\n`
    + `import type { Component } from 'vue'\n\n`
    + `export type LocalViewLoader = () => Promise<{ default: Component }>\n\n`
    + `export const localViewKeys = [\n${keyLines}\n] as const\n\n`
    + `export type LocalViewKey = typeof localViewKeys[number]\n\n`
    + `export const localViewLoaders: Readonly<Record<LocalViewKey, LocalViewLoader>> = {\n`
    + `${loaderLines}\n}\n`
}

const keys = (await collectIndexViews(viewsRoot)).map(viewKey).sort((left, right) => left.localeCompare(right))
assertUnique(keys)
const output = render(keys)
const current = await readFile(outputPath, 'utf8').catch(() => null)
if (current !== output) {
  if (checkOnly) {
    throw new Error('generated local Admin view registry is stale; run npm run routes:generate')
  }
  await writeFile(outputPath, output, 'utf8')
}
process.stdout.write(`${checkOnly ? 'verified' : 'generated'} ${keys.length} local Admin view loaders\n`)
